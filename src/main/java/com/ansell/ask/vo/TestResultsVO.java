package com.ansell.ask.vo;

public class TestResultsVO {

	private String testId = null;
	private String testName = null;
	private String testValue = null;
	private String testCategory = null;
	private String testCategoryId = null;
	private String testHighlightLogic = null;
	
	
	/**
	 * @return the testId
	 */
	public String getTestId() {
		return testId;
	}
	/**
	 * @param testId the testId to set
	 */
	public void setTestId(String testId) {
		this.testId = testId;
	}
	/**
	 * @return the testName
	 */
	public String getTestName() {
		return testName;
	}
	/**
	 * @param testName the testName to set
	 */
	public void setTestName(String testName) {
		this.testName = testName;
	}
	/**
	 * @return the testValue
	 */
	public String getTestValue() {
		return testValue;
	}
	/**
	 * @param testValue the testValue to set
	 */
	public void setTestValue(String testValue) {
		this.testValue = testValue;
	}
	public String getTestCategory() {
		return testCategory;
	}
	public void setTestCategory(String testCategory) {
		this.testCategory = testCategory;
	}
	public String getTestCategoryId() {
		return testCategoryId;
	}
	public void setTestCategoryId(String testCategoryId) {
		this.testCategoryId = testCategoryId;
	}
	public String getTestHighlightLogic() {
		return testHighlightLogic;
	}
	public void setTestHighlightLogic(String testHighlightLogic) {
		this.testHighlightLogic = testHighlightLogic;
	}
}
