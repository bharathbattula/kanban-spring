package com.ansell.ask.vo;

public class ProductSpecificationVO {

	private String prodSpecificationId = null;
	private String prodSpecificationName = null;
	private String prodSpecificationValue = null;
	
	/**
	 * @return the prodSpecificationId
	 */
	public String getProdSpecificationId() {
		return prodSpecificationId;
	}
	/**
	 * @param prodSpecificationId the prodSpecificationId to set
	 */
	public void setProdSpecificationId(String prodSpecificationId) {
		this.prodSpecificationId = prodSpecificationId;
	}
	/**
	 * @return the prodSpecificationName
	 */
	public String getProdSpecificationName() {
		return prodSpecificationName;
	}
	/**
	 * @param prodSpecificationName the prodSpecificationName to set
	 */
	public void setProdSpecificationName(String prodSpecificationName) {
		this.prodSpecificationName = prodSpecificationName;
	}
	/**
	 * @return the prodSpecificationValue
	 */
	public String getProdSpecificationValue() {
		return prodSpecificationValue;
	}
	/**
	 * @param prodSpecificationValue the prodSpecificationValue to set
	 */
	public void setProdSpecificationValue(String prodSpecificationValue) {
		this.prodSpecificationValue = prodSpecificationValue;
	}
}
